export function translate(locale: Locale, key: string, replacements?: Record<string, string | number>): string {
    const template = locale === 'ar' ? (arabicTranslations[key] ?? key) : key;

    if (!replacements) {
        return template;
    }

    return Object.entries(replacements).reduce((translated, [placeholder, value]) => {
        return translated.replaceAll(`{${placeholder}}`, String(value));
    }, template);
}

function normalizeDurationUnit(unit: string): 'day' | 'month' | 'year' | null {
    const normalized = unit.trim().toLowerCase();

    if (normalized === 'day' || normalized === 'days') {
        return 'day';
    }

    if (normalized === 'month' || normalized === 'months') {
        return 'month';
    }

    if (normalized === 'year' || normalized === 'years') {
        return 'year';
    }

    return null;
}

export function localizeDurationLabel(locale: Locale, label: string): string {
    const match = label.trim().match(/^(\d+)\s+(day|days|month|months|year|years)$/i);

    if (!match) {
        return translate(locale, label);
    }

    const value = Number(match[1]);
    const unit = normalizeDurationUnit(match[2]);

    if (!unit) {
        return translate(locale, label);
    }

    const key = value === 1 ? unit : `${unit}s`;

    return `${value} ${translate(locale, key)}`;
}

export function localizeCountdownLabel(locale: Locale, label: string): string {
    const trimmed = label.trim();

    if (trimmed === 'Ends today') {
        return translate(locale, trimmed);
    }

    const overdueMatch = trimmed.match(/^(.*)\s+overdue$/i);

    if (overdueMatch) {
        return `${localizeDurationLabel(locale, overdueMatch[1].trim())} ${translate(locale, 'overdue')}`;
    }

    const leftMatch = trimmed.match(/^(.*)\s+left$/i);

    if (leftMatch) {
        const parts = leftMatch[1]
            .split(',')
            .map((part) => localizeDurationLabel(locale, part.trim()))
            .join(locale === 'ar' ? '، ' : ', ');

        return `${parts} ${translate(locale, 'left')}`;
    }

    return translate(locale, trimmed);
}
